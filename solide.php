	<h1>Open source</h1>
 
	<h2>Définition</h2>
	<h3>Une culture libre</h3>

	<p><em>&ldquo;Je peux expliquer le logiciel libre en 3 mots liberté, égalité, fraternité&rdquo;</em> <a href="Christopher">^1</a>
	Originairement, le terme <em>open source</em> caractérise l'ouverture d'un logiciel se fondant sur un accès libre au code source. A l'inverse, un logiciel propriétaire est un programme fermé: le code source est inaccessible. Aujourd'hui, le terme d'open source s'applique à des domaines plus large que l'informatique et les logiciels, la <em>culture libre</em> s'est étendu au champ de l'éducation, des connaissances, de la création&hellip; La culture <em>libre</em>  est un mouvement idéologique qui lutte pour l'ouverture des objets numériques pour permettre leur utilisation, leur modification et leur libre circulation.</p>
	<img class="image" src="img/img4.jpg" title="Mou" alt="alt text" />	
<p>Si les logiciels libres (VLC, LibreOffice, WordPress&hellip;) se sont imposés parmi les programmes les plus rependus, le libre s'est d'autre part étendu aux moteurs de recherche (Firefox), aux système d'exploitation (GNU/Linux), aux encyclopédies (Wikipédia), aux monnaies (Bitcoin), au design (DIT: DoItYourself, LibreObjet, OSP) et d'autre domaines (Libre Graphic Magazine, cyberfeminism par Femke Snelting de Constant).</p>

