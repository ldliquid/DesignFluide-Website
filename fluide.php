  <div class="inner">
    <p>Ainsi, par système adaptatif, j'entends une création qui passe par des logiques qui définissent le comportement des éléments en fonction de leur nature (image, texte, légende). Il ne s'agit plus de positionner des formes les unes en fonction des autres mais de définir des actions pour chaque élément de même nature. De ce fait, je ne fige pas les éléments, je leur donne au contraire un mouvement, une action. J'illustre cette idée en m’appuyant sur le site Il tempo del postino sur lequel j'ai travaillé avec les graphistes du studio Metahaven. En effet, nous n'avons pas créé des compositions, nous avons créé des règles correspondant à chaque élément. Les textes, par exemple, apparaissent en superposition située à la moitié de la fenêtre du navigateur, les titres sont toujours en bas à gauche, les vidéos et les menus sont centrés. De cette manière le site Internet se compose à partir des régles du jeu. Ainsi, pour être cohérent sur Internet, le graphiste crée des logiques visuelles fluides qu'il ne compose pas solidement.
