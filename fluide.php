  <header>Il existe une mutation dans la façon d'envisager les formes : on passe d'une création immuable, dépendante d'une grille, vers une création qui repose sur des systèmes de compositions adaptatifs. Dans le premier cas il s'agit de travailler une image, dans le second de créer une logique graphique, un système cohérent.
